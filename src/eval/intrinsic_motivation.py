    nl = agg["n_learn"][0]
    e = [f'<svg xmlns="http://www.w3.org/2000/svg" width="{W_}" height="{H}" font-family="Segoe UI, Arial">',
         f'<rect width="{W_}" height="{H}" fill="#ffffff"/>',
         '<text x="20" y="26" font-size="15" font-weight="800" fill="#0b1324">'
         'Intrinsic motivation: self-directed mastery emerges &#8212; and escapes the noisy-TV trap</text>',
         '<text x="20" y="45" font-size="10.5" fill="#5b6b8c">no external reward, no goal &#8212; the agent acts only '
         'to improve its own world model; behaviour is measured, never imposed</text>']
    col = {"progress": "#2ca25f", "novelty": "#e6842a", "random": "#8c8c8c"}
    lab = {"progress": "learning\nprogress", "novelty": "pure\nnovelty", "random": "random"}
    # left: mastery emerges (A)
    bx, by, bh, bw = 44, 100, 150, 46
    e.append(f'<text x="{bx}" y="{by-8}" font-size="11" font-weight="700" fill="#28324a">cells mastered (of {nl:.0f})</text>')
    for i, d in enumerate(DRIVES):
        v = agg[f"mastered_{d}"][0]; x = bx + i * (bw + 10); h = v / nl * bh
        e.append(f'<rect x="{x}" y="{by+bh-h:.0f}" width="{bw}" height="{h:.0f}" fill="{col[d]}" opacity="0.85"/>')
        e.append(f'<text x="{x+bw/2:.0f}" y="{by+bh-h-5:.0f}" font-size="11" font-weight="700" fill="#0b1324" text-anchor="middle">{v:.0f}</text>')
        for li, part in enumerate(lab[d].split("\n")):
            e.append(f'<text x="{x+bw/2:.0f}" y="{by+bh+13+li*11:.0f}" font-size="9" fill="#28324a" text-anchor="middle">{part}</text>')
    e.append(f'<line x1="{bx-4}" y1="{by+bh}" x2="{bx+3*(bw+10):.0f}" y2="{by+bh}" stroke="#33415c"/>')
    e.append(f'<text x="{bx}" y="{by+bh+38:.0f}" font-size="8.5" fill="#5b6b8c">drive organises exploration</text>')
    # middle: noisy-TV trap (B)
    m0 = 300; mw = 60
    e.append(f'<text x="{m0}" y="{by-8}" font-size="11" font-weight="700" fill="#28324a">time trapped on the noise</text>')
    for i, d in enumerate(["progress", "novelty"]):
        v = agg[f"dwell_{d}"][0]; x = m0 + i * (mw + 18); h = v / 0.4 * bh
        e.append(f'<rect x="{x}" y="{by+bh-h:.0f}" width="{mw}" height="{h:.0f}" fill="{col[d]}" opacity="0.85"/>')
        e.append(f'<text x="{x+mw/2:.0f}" y="{by+bh-h-5:.0f}" font-size="11" font-weight="700" fill="#0b1324" text-anchor="middle">{v*100:.0f}%</text>')
        for li, part in enumerate(lab[d].split("\n")):
            e.append(f'<text x="{x+mw/2:.0f}" y="{by+bh+13+li*11:.0f}" font-size="9" fill="#28324a" text-anchor="middle">{part}</text>')
    e.append(f'<line x1="{m0-4}" y1="{by+bh}" x2="{m0+2*(mw+18):.0f}" y2="{by+bh}" stroke="#33415c"/>')
    e.append(f'<text x="{m0}" y="{by+bh+38:.0f}" font-size="8.5" fill="#5b6b8c">novelty is fooled by noise</text>')
    # right: efficiency (C)
    rx = 540; rw = 48
    e.append(f'<text x="{rx}" y="{by-8}" font-size="11" font-weight="700" fill="#28324a">steps to 90% mastery</text>')
    mxs = max(agg[f"steps90_{d}"][0] for d in DRIVES) * 1.1
    for i, d in enumerate(DRIVES):
        v = agg[f"steps90_{d}"][0]; x = rx + i * (rw + 10); h = v / mxs * bh
        e.append(f'<rect x="{x}" y="{by+bh-h:.0f}" width="{rw}" height="{h:.0f}" fill="{col[d]}" opacity="0.85"/>')
        e.append(f'<text x="{x+rw/2:.0f}" y="{by+bh-h-5:.0f}" font-size="10" font-weight="700" fill="#0b1324" text-anchor="middle">{v:.0f}</text>')
        for li, part in enumerate(lab[d].split("\n")):
            e.append(f'<text x="{x+rw/2:.0f}" y="{by+bh+13+li*11:.0f}" font-size="9" fill="#28324a" text-anchor="middle">{part}</text>')
